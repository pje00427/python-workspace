# 산술연산
a=21
b=2
print(a+b)
print(a-b)
print(a*b)
print(a**b) # 제곱
print(a/b) # 실수형 그 자체로
print(a/b) # 몫
print(a%b) # 나머지

# 비교연산(동등비교, 대소비교)
a, b=5, 3
print(a == b)
print(a != b)
print(a > b)
print(a <= b)
print(a is b)
print(a is not b)

# 논리연산
print(True or False)
print(False and True)

# 논리 부정 연산
print(not False)

list01 = list(range(10,20)) # 10~19
print(list01)

list02 = list(range(100)) # 0~99
print(list02)

print(list02[2])

# 범위 연산
print(list02[12:49])


